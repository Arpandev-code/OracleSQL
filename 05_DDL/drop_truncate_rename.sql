/*
DROP->
Drop command delete the table existence completely
i.e. drop statement destroys the existing database objects of that
particular table,index,view

Restoring a dropped table using FLASHBACK command->
FLASHBACK TABLE table_name TO BEFORE DROP;
Avilable from Oracle 11g version

Dropping Table Permanently: PURGE Command_>
DROP table table_name PURGE;

TRUNCATE command_>
The Truncate command will make the table empty i.e. all the TABLES
data will be deleted but the structure and database object is still alive and the TABLES
can be reused normally

TRUNCATE Table table_name;

Renaming a table: RENAME command->
RENAME old table_name to new_table_name;
*/



